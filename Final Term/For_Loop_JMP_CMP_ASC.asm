.MODEL SMALL
.STACK 100H
.DATA


.CODE
MAIN PROC
    
  MOV AH,1
  INT 21H
  MOV BL,AL
  
  MOV CL,'1'  ;LOOP INTIATE ALWAYS BE IN COUNTER REGISTER    ,HERE CL IS i
  
  FOR: 
  
  CMP CL,BL
  JG EXIT:
           
  MOV AH,2
  MOV DL,CL
  INT 21H
  
  INC CL
  JMP FOR:

    
    EXIT:
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN