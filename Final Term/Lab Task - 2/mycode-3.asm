.MODEL SMALL
.STACK 100H
.DATA
    MSG1 DB "INPUT HEXA: $"
    MSG2 DB 10,13,"RESULT AFTER SETTING: $"
    HEX_VALUE DB ?
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9
    LEA DX,MSG1
    INT 21H
    
    MOV AH,1
    INT 21H
    MOV CL,AL
    SUB AL, '0'
    CMP AL,9
    JBE DIGIT1
    SUB AL,7
DIGIT1:
    SHL AL,4
    MOV BH,AL
    
    MOV AH,1
    INT 21H
    MOV CH,AL
    SUB AL,'0'
    CMP AL,9  
    JBE DIGIT2
    SUB AL,7
DIGIT2:
    OR BH,AL
    MOV HEX_VALUE,BH
    AND BH, 0F0H
    MOV AH,9
    LEA DX,MSG2
    INT 21H
    
    MOV AL,BH
    SHR AL,4
    ADD AL,'0'
    CMP AL,'9'
    JBE DISP_HIGH
    ADD AL,7
DISP_HIGH:
    MOV DL,AL
    MOV AH,2
    INT 21H
    
    MOV AL,BH
    AND AL,0FH
    ADD AL, '0'
    CMP AL, '9'
    JBE DISP_LOW
    ADD AL, 7
DISP_LOW:
    MOV DL, AL
    MOV AH, 2
    INT 21H

    EXIT:
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN