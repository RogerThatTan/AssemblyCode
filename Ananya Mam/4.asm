.MODEL SMALL
.STACK 100H
.DATA
    
    MSG1 DB "ENTER A NUMBER: $"
    MSG2 DB 10,13,"POSITIVE$"
    MSG3 DB 10,13,"NEGATIVE$"
    MSG4 DB 10,13,"ZERO$"
    
.CODE 
MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    
    
    MOV AH,9
    LEA DX,MSG1
    INT 21H
    
    MOV AH,1
    INT 21H
    CMP AL, "-"
    JE  READ_NEGATIVE
    
    SUB AL,30H
    MOV BL,AL
    JMP CHECK_NUMBER
    
READ_NEGATIVE:
    
    MOV AH,1
    INT 21H
    SUB AL,30H
    NEG AL
    MOV BL,AL
    
CHECK_NUMBER:
    
    CMP BL,0
    JE IS_ZERO
    JL IS_NEGATIVE

IS_POSITIVE:
    MOV AH,9
    LEA DX,MSG2
    INT 21H
    JMP EXIT: 
    
IS_NEGATIVE:

    MOV AH,9
    LEA DX,MSG3
    INT 21H
    JMP EXIT:

IS_ZERO:

    MOV AH,9
    LEA DX,MSG4
    INT 21H
    JMP EXIT:  
    
EXIT:  
    
    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN 
 