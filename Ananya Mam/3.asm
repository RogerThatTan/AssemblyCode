.MODEL SMALL 
.STACK 100H
.DATA
 
 MSG1 DB "ENTER THREE LETTERS IN UPPERCASE: $"
 MSG2 DB 10,13, "THE OUTPUT IN LOWERCASE: $"

  


.CODE
MAIN PROC
   
   MOV AX,@DATA
   MOV DS,AX
   
   MOV AH,9
   LEA DX,MSG1
   INT 21H
   
   MOV AH,1
   INT 21H
   MOV BL,AL
   
   MOV AH,1
   INT 21H
   MOV BH,AL
    
    
   MOV AH,1
   INT 21H
   MOV CH,AL
   
   ADD BL,32
   ADD BH,32
   ADD CH,32 
   
   
   MOV AH,9
   LEA DX,MSG2
   INT 21H
   
   MOV AH,2
   MOV DL,BL
   INT 21H
    
   MOV AH,2
   MOV DL,BH
   INT 21H 
   
   MOV AH,2
   MOV DL,CH
   INT 21H
    

    MAIN ENDP
END MAIN