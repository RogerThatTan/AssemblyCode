.MODEL SMALL
.STACK 100H
.DATA

    MSG1 DB "ENTER A NUMBER: $"
    MSG2 DB 10,13,"GREATER THAN 5$"
    MSG3 DB 10,13,"LESS THAN 5$"
    MSG4 DB 10,13,"EQUAL TO 5$"

.CODE
MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9
    LEA DX,MSG1
    INT 21H
    
    MOV AH,1
    INT 21H
    SUB AL,30H
    MOV BL,AL
    
    CMP BL,5
    JE SOMAN
    JG BORO
    
    JL:
    MOV AH,9
    LEA DX,MSG3
    INT 21H
    JMP EXIT:
    
    SOMAN:
    MOV AH,9
    LEA DX,MSG4
    INT 21H
    JMP EXIT:
    
    BORO:
    MOV AH,9
    LEA DX,MSG2
    INT 21H
    JMP EXIT:
    
    EXIT:
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN